import numpy as np

# Define the vectors a and b
a = np.array([1, 5, -3, 2])  # Vector a
b = np.array([8, 2, 4, 7])  # Vector b

# Calculate the dot product of a and b using np.dot()
dot_product = np.dot(a, b)

# Print the result
print("The dot product of vectors a and b is:", dot_product)